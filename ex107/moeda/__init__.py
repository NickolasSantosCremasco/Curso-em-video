
def moeda(n):
    return f'R${n:.2f}'

def metade(m):

    return m / 2

def dobro(m):
    return m * 2

def aumentar(n, p):
    porcentagem = (n * p) / 100
    final = n + porcentagem
    return final

def diminuir(n,p):
    porcentagem = (n * p) / 100
    final = n - porcentagem
    return final

